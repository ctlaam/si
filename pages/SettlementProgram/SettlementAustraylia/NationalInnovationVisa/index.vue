                            <h2><span style="font-weight: 400;">Giới thiệu về Visa Đổi mới Quốc gia (National Innovation
                                    Visa)</span></h2>
                            <p><span style="font-weight: 400;">Visa Đổi mới Quốc gia (National Innovation Visa) là thị
                                    thực thường trú Úc dành cho những cá nhân có tài năng đặc biệt xuất chúng từ khắp
                                    nơi trên thế giới. Những cá nhân này sẽ giúp nước Úc tạo ra cơ hội việc làm cũng như
                                    thúc đẩy tăng trưởng năng suất trong các lĩnh vực quan trọng của nền kinh tế
                                    Úc.&nbsp;</span></p>
                            <p><span style="font-weight: 400;">National Innovation Visa ưu tiên nhắm đến 4 đối tượng
                                    được xem là nhóm tài năng đặc biệt chính sau đây:&nbsp;</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Nhà nghiên
                                        cứu toàn cầu: Ứng viên cần chứng minh thành tích nghiên cứu với các ấn phẩm quan
                                        trọng trên các tạp chí hàng đầu, có trích dẫn đáng tin cậy và các giải thưởng
                                        hàng đầu trong lĩnh vực của mình.&nbsp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Doanh nhân:
                                        Doanh nhân đã thành đạt và mới nổi đều đủ điều kiện, đặc biệt là những người đã
                                        chứng minh được sự thành công trong các sáng kiến ​​do Tiểu bang và Vùng lãnh
                                        thổ Úc khởi xướng.&nbsp;&nbsp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Nhà đầu tư
                                        sáng tạo: Không như các loại thị thực trước đây, chủ yếu tập trung vào ngưỡng
                                        đầu tư, loại thị thực này chú trọng hơn vào chất  lượng  đầu tư, ưu tiên những
                                        người có đóng góp có ý nghĩa cho các lĩnh vực kinh tế và sáng tạo của
                                        Úc.&nbsp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Vận động
                                        viên và nhà sáng tạo: Các vận động viên và nhà sáng tạo xuất sắc, đặc biệt là
                                        những người đại diện cho Úc trên trường quốc tế, cũng có thể nộp đơn, góp phần
                                        vào hình ảnh toàn cầu và sự phát triển của Úc trong lĩnh vực tương ứng của
                                        họ.&nbsp;</span></li>
                            </ul>
                            <p><span style="font-weight: 400;">Lợi ích nổi bật dành cho nhà đầu tư và gia đình khi tham
                                    gia National Innovation Visa:</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Trực tiếp
                                        trở thành thường trú nhân: Tự do sinh sống, học tập và làm việc tại Úc vô thời
                                        hạn. Đồng thời, được hưởng các chế độ phúc lợi y tế như Medicare và an sinh xã
                                        hội gần đầy đủ như công dân Úc.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Không giới
                                        hạn độ tuổi đối với ứng viên nộp đơn tham gia.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Linh hoạt
                                        trong yêu cầu trình độ tiếng Anh: Nghĩa là ứng viên có thể đáp ứng yêu cầu tiếng
                                        Anh hoặc nộp thêm tiền đều được.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Không giới
                                        hạn lĩnh vực nghề nghiệp: Dành cho tất cả cá nhân có tài năng đặc biệt xuất
                                        chúng ở nhiều lĩnh vực khác nhau, miễn là đáp ứng đầy đủ tiêu chí về thành tích
                                        nổi bật, khả năng sáng tạo, có thể đóng góp cho sự phát triển của nước
                                        Úc.&nbsp;</span></li>
                            </ul>
                            <h2><span style="font-weight: 400;">National Innovation Visa ưu tiên cho các nhóm đối tượng
                                    nào?</span></h2>
                            <p><span style="font-weight: 400;">Việc xem xét hồ sơ National Innovation Visa được ưu tiên
                                    theo các thứ tự sau đây:</span></p>
                            <ul>
                                <li><b>Ưu tiên 1:</b><span style="font-weight: 400;"> Ứng viên xuất sắc trong bất kỳ
                                        lĩnh vực nào, hiện đang là chuyên gia toàn cầu và được nhận giải thưởng quốc tế
                                        hàng đầu trong lĩnh vực của mình (có thể là Giải Nobel, Huy chương vàng Olympic,
                                        v.v.)</span></li>
                                <li><b>Ưu tiên 2:</b><span style="font-weight: 400;"> Ứng viên đạt thành tích nổi bật
                                        trong bất kỳ lĩnh vực nào được đề cử theo Mẫu 1000 đã được phê duyệt bởi Liên
                                        bang, Tiểu bang hoặc Vùng lãnh thổ Úc. Bao gồm vận động viên, nhà sáng tạo quốc
                                        tế, nhà đầu tư sáng tạo, v.v.&nbsp;</span></li>
                                <li><b>Ưu tiên 3: </b><span style="font-weight: 400;">Ứng viên đạt thành tích đặc biệt
                                        nổi bật trong lĩnh vực sau đây: Công nghệ mũi nhọn, Chăm sóc sức khỏe, Năng
                                        lượng tái tạo và công nghệ phát thải thấp</span></li>
                                <li><b>Ưu tiên 4: </b><span style="font-weight: 400;">Ứng viên đạt thành tích đặc biệt
                                        nổi bật trong lĩnh vực sau đây: Thực phẩm và công nghệ nông nghiệp; Năng lực
                                        quốc phòng, Không gian; Giáo dục; Dịch vụ và Công nghệ Tài chính; Cơ sở Hạ tầng
                                        và Giao thông Vận tải và Tài nguyên Môi trường.</span></li>
                                <li style="list-style-type: none;">&nbsp;</li>
                            </ul>
                            <h2><span style="font-weight: 400;">Cập nhật thông tin Visa Đổi mới Quốc gia (National
                                    Innovation Visa) mới nhất</span></h2>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Visa Đổi
                                        mới Quốc gia 858, trước đây có tên gọi là Visa Tài năng Toàn cầu 858.</span>
                                </li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Yêu cầu về
                                        Thư mời nộp hồ sơ: Ứng viên cần có Thư mời nộp hồ sơ (ITA) bằng văn bản từ Bộ
                                        trưởng Bộ Di trú Úc để nộp đơn xin cấp National Innovation Visa trong khoảng
                                        thời gian quy định.&nbsp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Ứng viên
                                        phải chứng minh thành tích đặc biệt nổi bật được công nhận trên toàn thế giới
                                        như đã nêu trong Thư mời nộp hồ sơ.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Xóa bỏ yêu
                                        cầu xác nhận vai trò Đặc phái viên của Thủ tướng về Kinh doanh Toàn cầu và Thu
                                        hút Nhân tài do ít được sử dụng đến kể từ khi giới thiệu.&nbsp;</span></li>
                            </ul>
                            <p><span style="font-weight: 400;">Lưu ý quan trọng là các hồ sơ xin Visa Tài năng Toàn cầu
                                    858 đã được nộp trước ngày 06/12/2024 sẽ được đánh giá và xem xét theo quy định tại
                                    thời điểm nộp hồ sơ. Còn các thay đổi của Visa Đổi mới Quốc gia 858 chỉ áp dụng đối
                                    với các hồ sơ được nộp từ hoặc sau ngày 06/12/2024. Việc áp dụng mức phí cũng thay
                                    đổi theo, mức lệ phí hồ sơ National Innovation Visa mới là 4,840 AUD.&nbsp;</span>
                            </p>
