                            <h2><strong>Định cư Mỹ EB-3 là gì?</strong></h2>
                            <p><span style="font-weight: 400;">Visa EB-3 là chương trình định cư Mỹ diện tay nghề do
                                    Chính phủ Mỹ ban hành vào năm 1993 nhằm thu hút lao động nước ngoài và gia đình gồm
                                    vợ/chồng và con cái dưới 21 tuổi còn độc thân đến Mỹ sinh sống, học tập và làm việc
                                    lâu dài. Chương trình <a
                                        href="https://sigroup.vn/tin-tuc/di-my-dien-eb-3-nhung-dieu-can-luu-y-khi-lam-thu-tuc/"
                                        target="_blank" rel="noopener">Visa EB-3</a> bao gồm 3 diện chính:&nbsp;</span>
                            </p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Lao động
                                        lành nghề (EB-3 Skilled Workers);</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Lao động
                                        phổ thông (EB-3 Unskilled Workers);</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Lao động
                                        chuyên gia (EB-3 Professionals).</span></li>
                            </ul>
                            <h2><strong>Định cư Mỹ diện EB3 chi phí bao nhiêu?</strong></h2>
                            <p><span style="font-weight: 400;">Dưới đây là lệ phí nộp đơn xin cấp Visa EB-3:</span></p>
                            <p><b>Trường hợp nộp đơn xin cấp Visa EB-3 tại Mỹ:</b></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><a
                                        href="https://www.uscis.gov/sites/default/files/document/forms/i-140.pdf"
                                        target="_blank" rel="noopener"><span style="font-weight: 400;">Mẫu
                                            I-140</span></a><span style="font-weight: 400;"> (Đơn xin nhập cư dành cho
                                        lao động nước ngoài): 715 USD.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Phí nhập
                                        cư: 235 USD.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><a
                                        href="https://www.uscis.gov/sites/default/files/document/forms/i-485.pdf"
                                        target="_blank" rel="noopener"><span style="font-weight: 400;">Mẫu
                                            I-485</span></a><span style="font-weight: 400;"> (Đơn xin điều chỉnh tình
                                        trạng cư trú): 1,440 USD. </span></li>
                            </ul>
                            <p><b>Trường hợp nộp đơn xin cấp Visa EB-3 từ bên ngoài nước Mỹ:</b></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Mẫu I-140
                                        (Đơn xin nhập cư dành cho lao động nước ngoài): 715 USD.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Mẫu DS-260:
                                        345 USD.</span></li>
                                <li style="font-weight: 400;" aria-level="1"><a
                                        href="https://www.uscis.gov/sites/default/files/document/forms/i-864.pdf"
