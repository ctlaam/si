                            <p><em><strong><span style="font-size: 32px;">FAQs</span></strong></em></p>
                            <h2><strong>Express Entry là gì?</strong></h2>
                            <p><span style="font-weight: 400;">Express Entry là hệ thống nộp đơn online mà Bộ Di trú, Tị
                                    nạn và Quốc tịch Canada (IRCC) sử dụng để quản lý các đơn xin định cư Canada từ lao
                                    động tay nghề cao muốn sinh sống và làm việc lâu dài tại đất nước này.&nbsp;</span>
                            </p>
                            <h2><strong>Các chương trình Express Entry Canada phổ biến</strong></h2>
                            <p><span style="font-weight: 400;">Định cư Canada diện Express Entry bao gồm 3 chương trình
                                    phổ biến sau đây:&nbsp;</span></p>
                            <p><b>1. Chương trình Tay nghề Kinh nghiệm Canada (CEC)</b></p>
                            <p><span style="font-weight: 400;">Ứng viên cần đáp ứng các yêu cầu dưới đây:&nbsp;</span>
                            </p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu 1
                                        năm kinh nghiệm làm việc toàn thời gian (hoặc kinh nghiệm bán thời gian tương
                                        đương) ở Canada trong vòng 3 năm trước khi nộp đơn;&nbsp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Có kinh
                                        nghiệm làm việc trong thời gian tạm trú tại Canada;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Nhóm nghề
                                        nghiệp nằm trong danh sách TEER NOC;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Yêu cầu
                                        ngoại ngữ: Tiếng Anh (CLB 5 hoặc 7) hoặc tiếng Pháp (NCLC 5 hoặc 7) tùy ngành
                                        nghề;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu
                                        tốt nghiệp THPT và nếu học ở ngoài Canada thì bằng cấp cần được đánh giá đáp ứng
                                        tiêu chuẩn Canada.</span></li>
                            </ul>
                            <p><b>2. Chương trình Tay nghề cao Liên bang (FSWP)</b></p>
                            <p><span style="font-weight: 400;">Ứng viên cần đáp ứng các yêu cầu dưới đây:</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu 1
                                        năm kinh nghiệm làm việc toàn thời gian công việc thuộc Danh mục TEER NOC trong
                                        vòng 10 năm qua;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Yêu cầu
                                        ngoại ngữ: CLB 5 (tiếng Anh) hoặc NCLC 5 (tiếng Pháp);</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu
                                        tốt nghiệp THPT (trường hợp học ở ngoài Canada, bằng cấp cần được đánh giá đáp
                                        ứng tiêu chuẩn Canada);</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Chứng minh
                                        tài chính hoặc nhận Thư mời làm việc hợp lệ tại Canada. </span></li>
                            </ul>
                            <p><b>3. Chương trình Tay nghề Nhóm thợ Liên bang (FSTP)</b></p>
                            <p><span style="font-weight: 400;">Ứng viên cần đáp ứng các yêu cầu dưới đây:</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu 2
                                        năm kinh nghiệm làm việc toàn thời gian (hoặc kinh nghiệm bán thời gian tương
                                        đương) trong lĩnh vực liên quan Phân loại Nghề nghiệp Quốc gia (NOC) trong vòng
                                        5 năm trước khi nộp đơn;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Nhận Thư
                                        mời làm việc hoặc Chứng nhận trình độ chuyên môn phù hợp;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Yêu cầu
                                        ngoại ngữ: Tiếng Anh (Nghe – Nói CLB 5 và Đọc – Viết CLB 4) hoặc tiếng Pháp
                                        (Nghe – Nói NCLC 5 và Đọc – Viết NCLC 4);</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Tối thiểu
                                        tốt nghiệp THPT và nếu học ở ngoài Canada thì bằng cấp cần được đánh giá đáp ứng
                                        tiêu chuẩn Canada;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Chứng minh
                                        tài chính hoặc nhận Thư mời làm việc hợp lệ tại Canada.</span></li>
                            </ul>
                            <h2><strong>CRS là gì? Cách tính điểm Express Entry Canada 2024?</strong></h2>
                            <p><span style="font-weight: 400;">CRS là hệ thống tính điểm được sử dụng để đánh giá và
                                    chấm điểm hồ sơ Express Entry của ứng viên nhằm xếp hạng ứng viên này so với các ứng
                                    viên khác trong nhóm. CRS đánh giá thông tin hồ sơ mà ứng viên nộp, bao gồm kỹ năng,
                                    kinh nghiệm làm việc, trình độ ngoại ngữ, trình độ học vấn và các yếu tố
                                    khác.</span></p>
                            <p><span style="font-weight: 400;">Cách tính điểm Express Entry Canada 2024 bao gồm 600 điểm
                                    chính và 600 điểm bổ sung, tổng cộng tối đa 1,200. Trong đó, 600 điểm chính được
                                    tính dựa trên các yếu tố sau:</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Yếu tố kinh
                                        nghiệm;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Trình độ
                                        học vấn;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Các yếu tố
                                        của vợ/chồng bao gồm trình độ ngoại ngữ và trình độ học vấn.&nbsp;</span></li>
                            </ul>
                            <p><span style="font-weight: 400;">Ngoài ra, 600 điểm bổ sung được tính dựa trên các yếu tố
                                    sau:&nbsp;</span></p>
                            <ul>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Bằng cấp
                                        hoặc chứng chỉ ở Canada;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Thư mời làm
                                        việc hợp lệ;</span></li>
                                <li style="font-weight: 400;" aria-level="1"><span style="font-weight: 400;">Nhận đề cử
