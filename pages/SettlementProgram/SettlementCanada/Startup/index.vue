                            <h2><strong>Start up Visa Canada (SUV Canada) là gì?</strong></h2>
                            <p><span style="font-weight: 400;">Chương trình định cư Canada diện khởi nghiệp (Start-up
                                    Visa) được Bộ Di trú Canada (IRCC) ban hành vào năm 2013 nhằm mục đích thu hút các
                                    doanh nhân nước ngoài có ý tưởng kinh doanh mới mẻ, mang tính đột phá nhập cư vào
                                    Canada. Định cư Canada diện SUV giúp doanh nhân bỏ qua các bước chờ cấp Giấy phép
                                    làm việc (WP) hoặc Thẻ tạm trú (TR) và trực tiếp nhận Thẻ thường trú PR cho cả gia
                                    đình mà không phụ thuộc vào kết quả kinh doanh của dự án khởi nghiệp.&nbsp;</span>
                            </p>
                            <p><span style="font-weight: 400;">Start up Visa Canada là chương trình định cư Canada dành
                                    cho các doanh nhân nhập cư có kỹ năng và tiềm năng xây dựng doanh nghiệp tại quốc
                                    gia này với điều kiện sáng tạo, có thể tạo việc làm cho công dân Canada và doanh
                                    nghiệp hoạt động có thể cạnh tranh trên quy mô toàn cầu.&nbsp;</span></p>
